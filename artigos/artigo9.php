          <p>
            Este trabalho relata uma experiência pedagógica realizada em 2018 com os alunos da EJA II, da EMEIEF Professora Sônia Aparecida Marques, durante as aulas de Língua Inglesa. O projeto, que teve como eixo norteador o trabalho e as leis trabalhistas, foi estruturado na proposta de um currículo integrador, que tem como base a perspectiva sócio-histórico-cultural de Lev Vygotsky (Vygotsky, 2001) e o trabalho como princípio educativo.          </p>
          <p>
            Para Vygotsky (2001), a mediação ocupa papel fundamental na aprendizagem e, para tanto, sujeito e mundo são tratados simultaneamente. O educando participa do processo ensino e aprendizagem como idealizador, autor e transformador do conhecimento e do mundo, refletindo sempre sobre sua ação.          
          </p>
          <p>
            Presente no Plano Municipal de Educação de Santo André (2015), o "trabalho como princípio educativo" visa uma formação que prepare o aluno para "o mundo do trabalho, estabelecendo inter-relações entre teoria e prática, nos eixos da Ciência, Trabalho, Tecnologia, Política, Cultura e Cidadania" (Meta 10.6). Deste modo, o estudante não é apenas formado para o trabalho, ele passa a refletir sobre dinâmicas de produção da sociedade moderna, buscando sua autonomia e emancipação.
          </p>
          <P>
            Nesta perspectiva, as aulas de Língua Inglesa foram pensadas para além do conhecimento linguístico, considerando também a dimensão intercultural. Neste caso, foram levados em conta, valores sócio-histórico-culturais dos países falantes deste idioma, refletindo criticamente sobre as ideologias que estão presentes nessas sociedades para compreender como elas influenciam outras nações (Currículo da EJA da Prefeitura de Santo André, 2018).
          </P>
          <p>
            A fim de colocar esta proposta em prática, foram utilizadas a dialogicidade e a investigação de temas geradores junto aos educandos, conforme apontado pela metodologia de Paulo Freire (1968).
          </p>
          <p>
            O tema de estudo sugerido pelos estudantes foi "o trabalho e as novas leis trabalhistas". Em Língua Inglesa, a proposta seria estudar como o assunto se apresenta na sociedade estadunidense. No entanto, a concepção utópica de uma realidade norte-americana sempre próspera, apresentada pelos alunos no início do projeto, fez com que o foco de estudo fosse direcionado a responder o que é o "sonho americano" e quem beneficia-se dele, considerando-se o trabalhador latino-americano nos Estados Unidos.
          </p>
          <p>
            Para sistematizar os conhecimentos desenvolvidos ao longo do projeto, foi proposto que os educandos desenvolvessem um "Jogo da Vida", nos moldes do jogo original de tabuleiro, mostrando a trajetória desses trabalhadores. 
          </p>
          <p>
            Ao final do projeto era esperado que os estudantes fossem capazes de: apropriar-se do conceito "American dream" e desmistificá-lo; conhecer direitos e benefícios concedidos pelo governo americano aos trabalhadores daquele país; refletir sobre como a política neoliberal influencia as condições de trabalho; e traçar paralelos entre a realidade americana e a brasileira.
          </p>
          <p>
            Durante as aulas, os alunos assumiram o papel de protagonistas, contribuindo ativamente com sugestões e questionamentos que norteavam as etapas seguintes. Deste modo, as aulas seguiram o método histórico crítico de educação.
          </p>
          <p>
            Por ter sido realizado em quatro grupos simultaneamente e por ter havido o apoio de outras áreas do conhecimento  -História e Geografia-, houve necessidade de planejamento e replanejamento constantes.
          </p>
          <p>
            Ao longo das atividades propostas, foi notória a apreensão dos conhecimentos por parte dos estudantes, que posicionavam-se criticamente acerca das políticas americanas e percebiam as semelhanças entre as leis trabalhistas americanas e o projeto de leis em discussão no Brasil. Logo, podemos afirmar que os alunos eram capazes de expor suas ideias, buscando referenciais naquilo que foi estudado, expressando-se com clareza. Portanto, é possível concluir que os resultados esperados neste projeto foram alcançados.
          </p>
          <p>
            Integração e interação entre as diversas áreas do conhecimento teve um caráter fundante para o sucesso deste trabalho. Assim, visando a emancipação do aluno da EJA, a atenção e o cuidado devem estar nos conteúdos que serão abordados e, principalmente, na forma de organizá-los e integrá-los ao mundo. Este constitui-se como um ponto de destaque deste trabalho e foi, ao mesmo tempo, o maior desafio na construção de projetos futuros.
          </p>
