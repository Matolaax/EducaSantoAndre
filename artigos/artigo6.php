          <P>
            Em determinados momentos das narrativas, as professoras destacaram ter dificuldades em realizar a avaliação com os alunos haitianos, principalmente a primeira sondagem, porque eles chegam sem entender a língua portuguesa. Outro fato é que o aluno pode já ter sido alfabetizado em sua língua materna, porém, este formato de avaliação é realizado em língua portuguesa, não dando a chance de mostrarem os conhecimentos prévios em sua língua materna, o que impede os professores de realizarem uma análise do quanto e o que eles já sabem ou supõem sobre a escrita alfabética, de modo geral.
          </P>
          <p>
            Pode-se perceber que havia uma preocupação das professoras com a cultura dessas crianças, evidenciando uma “postura pedagógica culturalmente sensível”, por meio de trabalhos desenvolvidos em sala de aula que permitiram aos alunos compartilharem a sua cultura por meio de música em crioulo haitiano cantada ou apresentação de dança. Porém, foram experiências ocasionais e espontâneas sem um planejamento ou um projeto mais amplo em PPP, pois as professoras, em muitos casos, não possuíam formação específica para se trabalhar questões relacionadas ao ensino em outra língua em contextos multiculturais.
