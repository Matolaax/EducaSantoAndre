          </p>
          <p>
            No município de Santo André as instituições da primeiríssima infância apresentam já em sua ornamentação a predominância da figura humana branca como modelo e norma de ser humano.        Esse caráter perpassa pelos materiais pedagógicos e a literatura infantil oferecida por essas instituições (literatura essa mantenedora dos pilares dos contos de fadas “tradicionais” produzidos como mecanismos culturais de base do racismo) e se estende inclusive ao brincar (bonecas, brinquedos essenciais na construção simbólica da criança pequena, quando não em sua totalidade, certamente em maioria, a escola as oferece como representação humana da pessoa branca).  
          </p>
