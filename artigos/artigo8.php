          </p>
          <P>
            Este projeto teve como foco colocar a vontade e a cultura das crianças como algo importante e possível de ser considerado como conteúdos importantes a serem desenvolvidos, pensados e praticados dentro da escola. Dessa forma tivemos a possibilidade das crianças experimentarem, conhecerem e praticarem jogos africanos, refletindo sobre semelhanças com as nossas brincadeiras populares,  danças com histórico de resistência e enfrentamento contra preconceitos, como por exemplo, samba, maculelê, break e o funk;  tiveram a possibilidade de assistir um filme relacionados a uma das formas de vida do continente africano, chamado “Kiriku e a feiticeira”, discutimos sobre as diversas faces do racismo dentro e fora da escola, por meio de episódios da série “Todo mundo odeia o Chris”, e fizemos uma oficina de Turbante, visando a valorização da estética africana, desconstruindo preconceitos relacionados a religiosidade de matriz africana, e principalmente a autoestima de meninas pretas que tem dificuldade de se reconhecer e se identificar com seus cabelos crespos.
          </P>
          <p>
            A Educação Física tem participação importante na formação dos corpos das crianças, que acontecem por meio de uma construção social, histórica e cultural, pensando especificamente nos corpos pretos, essa formação pode  e deve acontecer de maneira positiva pela democratização de conteúdos, visando ampliar as referências das práticas corporais, buscando desenvolver aulas com referências não só europeias, que acontecem de maneira naturalizada,  mas de origem africana também. Dessa forma podemos pensar em uma educação pública, de qualidade e antirracista.
