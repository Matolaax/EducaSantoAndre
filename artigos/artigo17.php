          <p>
            A primeira especificidade elencada foi a aquisição do sistema de escrita e leitura Braille (na máquina de escrever) em que muitas vezes o aluno não tinha o interlocutor para suas produções, já que o seu professor da sala regular desconhecia o sistema Braille, sendo assim um entrave no aprimoramento da compreensão das regras gramaticais e ortografia da língua portuguesa. A outra especificidade foi o uso do Soroban para cálculos matemáticos, também por desconhecimento deste instrumento pela professora da sala regular, ficando o aluno refém do uso de material concreto para realizar seus cálculos. Deste modo, decidiu-se por intensificar o AEE para esses alunos, ou seja, o atendimento no contraturno seria quatro vezes na semana, com o objetivo de acelerar a aquisição do sistema Braille e aprendizado do uso do soroban. Ademais, considerou-se a necessidade de intensificar a parceria com a professora da sala regular tanto na mediação, como na adaptação das atividades para impressão em Braille, principalmente na interlocução da professora com a produção escrita em Braille do seu aluno. Concluiu-se os primeiros arremates do projeto com o planejamento realizado em Reunião Pedagógica Semanal (RPS) entre a professora da sala regular, do AEE mediadas pela Assistente Pedagógica da Unidade Escolar e da Sala de Recursos Multifuncionais. A relação de parceria, onde a colaboração de fato ocorre, é uma conquista, muitas vezes estabelecida após inúmeras tentativas e desdobramentos. Este trabalho é uma construção que vai influenciar diretamente na repercussão e qualidade do trabalho, ou seja, no desenvolvimento e aprendizagem dos alunos. O desafio sobre a capacidade dos alunos e a possibilidade de aprenderem na escola regular ainda está sendo contestado, muito embora observa-se o contrário.
