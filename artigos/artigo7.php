          </p>
          <P>
            A escola tem um compromisso, junto com outros espaços sociais, de contribuir para a eliminação de atitudes e comportamentos preconceituosos e discriminatórios, pois trabalha com a formação de crianças e jovens com autoestima, respeito por si e pelos outros, para uma cidadania plena. Ao se discutir multiculturalismo e/ou interculturalidade é imperativo reconhecer a diversidade e a pluralidade que constituem a sociedade moderna. O Brasil, com seu histórico colonial, teve um processo que resultou em culturas híbridas, logo, a perspectiva de uma educação multicultural aparece como consequência de fenômenos que causaram impacto em diversos países: o movimento pelos direitos civis dos negros norte-americanos, na década de 1960; as correntes migratórias dos períodos entre as guerras; a colonização e a descolonização da Ásia e da África e as reivindicações do movimento estudantil, resultando em grandes denúncias de desigualdade, exclusão e incapacidade do Estado de resolver os problemas sociais. Esses processos repercutiram, sem dúvida, no ambiente escolar.          
          </P>
